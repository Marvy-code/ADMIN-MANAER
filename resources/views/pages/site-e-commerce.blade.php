                <h4 class='anal'>Les différentes formes de commerce électronique</h4>
                <p>
                    D'abord E-commerce et E-business sont deux concepts que l’on a tendance à confondre. En réalité,
                    ils n’ont rien à voir l’un avec l’autre.
                    <br>
                    <br>
                    Le e-commerce se réfère seulement à la transaction de biens et services entre un acheteur et
                    un vendeur. Le e-business, quant à lui, fait référence à l’ensemble du processus à mettre en
                    oeuvre afin de gérer un commerce en ligne.
                </p>
                <strong>
                    Chaque commerce se dirige à un type de client spécifique. Cela nous permet de distinguer les
                    types d’ e-commerce suivants :
                </strong>
                <br>
                <br>
                <ol type="square">
                    <li>
                        <strong>B2B (Business-to-Business) :</strong>
                        entreprises dont les clients finaux sont d’autres entreprises ou d’autres organisations.
                        Par exemple, une boutique de matériel de construction qui s’adresse à des architectes ou des
                        décorateurs d’intérieurs. <br> <br>
                    </li>
                    <li>
                        <strong>B2C (Business-to-Consumer) :</strong>
                        entreprises qui vendent directement les produits ou services aux consommateurs finaux.
                        C’est la forme la plus courante, et l’on dénombre une multitude d’exemples dans les domaines de
                        la mode, de l’électronique, etc. <br> <br>
                    </li>
                    <li>
                        <strong>C2B (Consumer-to-Business) :</strong>
                        portails sur lesquels les consommateurs proposent un produit ou un service que les
                        entreprises peuvent acquérir. Il s’agit des portails d’emploi freelance classiques comme Elance,
                        Odesk, Humaniance, Hopwork, etc. <br> <br>
                    </li>
                    <li>
                        <strong>C2C (Consumer-to-Consumer) :</strong>
                        entreprises qui facilitent la vente de produits entre consommateurs. Les exemples les
                        plus parlants sont eBay, Leboncoin ou tout portail de vente d’articles d’occasion entre particuliers.
                        <br>
                        <br>
                    </li>
